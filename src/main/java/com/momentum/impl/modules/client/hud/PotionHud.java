package com.momentum.impl.modules.client.hud;

/**
 * PotionHud config options
 *
 * @author linus
 * @since 02/11/2023
 */
public enum PotionHud {

    /**
     * Hides the effect HUD
     */
    HIDE,

    /**
     * Moves the hud elements around the effect HUD
     */
    MOVE,

    /**
     * Keeps the effect HUD
     */
    KEEP
}
