package com.momentum.impl.modules.render.norender;

/**
 * Item no render options
 *
 * @author linus
 * @since 03/01/2023
 */
public enum NoItems {

    /**
     * Prevents the dropped item from rendering
     */
    HIDE,

    /**
     * Removes the item from the world
     */
    REMOVE,

    /**
     * Render dropped items
     */
    OFF
}
